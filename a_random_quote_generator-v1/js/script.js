/***
 * `getRandomQuote` function generates random quotes from the quotes array
 * param: takes in an array of quotes 
 * return: returns a random quote from the index generated by a random number
 ***/
const getRandomQuote = ( arr ) => {
  let randomIndex = Math.floor( Math.random() * arr.length );
	return arr[ randomIndex ];
}

